        private List<Action>? _onReadyAction; 
        public ParallaxBackgroundAction OnReady(Action action, bool oneShot = false, bool deferred = false) {
            AddSignal(ref _onReadyAction, "ready", nameof(_GodotSignalReady), action, oneShot, deferred);
            return this;
        }

        public ParallaxBackgroundAction RemoveOnReady(Action action) {
            RemoveSignal(_onReadyAction, "ready", nameof(_GodotSignalReady), action);
            return this;
        }

        private ParallaxBackgroundAction _GodotSignalReady() {
            ExecuteSignal(_onReadyAction);
            return this;
