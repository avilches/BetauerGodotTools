namespace Betauer.GodotAction.Proxy {
    public class AnimationTreeAction : ProxyNode {

        private List<Action>? _onReadyAction; 
        public AnimationTreeAction OnReady(Action action, bool oneShot = false, bool deferred = false) {
            AddSignal(ref _onReadyAction, "ready", nameof(_GodotSignalReady), action, oneShot, deferred);
            return this;
        }

        public AnimationTreeAction RemoveOnReady(Action action) {
            RemoveSignal(_onReadyAction, "ready", nameof(_GodotSignalReady), action);
            return this;
        }

        private AnimationTreeAction _GodotSignalReady() {
            ExecuteSignal(_onReadyAction);
            return this;
        }

