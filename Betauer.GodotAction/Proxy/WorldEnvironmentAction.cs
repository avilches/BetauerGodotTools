    public class WorldEnvironmentAction : ProxyNode {

        private List<Action>? _onReadyAction; 
        public WorldEnvironmentAction OnReady(Action action, bool oneShot = false, bool deferred = false) {
            AddSignal(ref _onReadyAction, "ready", nameof(_GodotSignalReady), action, oneShot, deferred);
            return this;
        }

        public WorldEnvironmentAction RemoveOnReady(Action action) {
            RemoveSignal(_onReadyAction, "ready", nameof(_GodotSignalReady), action);
            return this;
        }

        private WorldEnvironmentAction _GodotSignalReady() {
            ExecuteSignal(_onReadyAction);
            return this;
        }

