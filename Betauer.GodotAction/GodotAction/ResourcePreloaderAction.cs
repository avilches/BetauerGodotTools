namespace Betauer.GodotAction {
    public class ResourcePreloaderAction : ProxyNode {

        private List<Action>? _onReadyAction; 
        public ResourcePreloaderAction OnReady(Action action, bool oneShot = false, bool deferred = false) {
            AddSignal(ref _onReadyAction, "ready", nameof(_GodotSignalReady), action, oneShot, deferred);
            return this;
        }

        public ResourcePreloaderAction RemoveOnReady(Action action) {
            RemoveSignal(_onReadyAction, "ready", nameof(_GodotSignalReady), action);
            return this;
        }

        private ResourcePreloaderAction _GodotSignalReady() {
            ExecuteSignal(_onReadyAction);
            return this;
        }

