        public CheckBoxAction OnButtonDown(Action action, bool oneShot = false, bool deferred = false) {
            AddSignal(ref _onButtonDownAction, "button_down", nameof(_GodotSignalButtonDown), action, oneShot, deferred);
            return this;
        }

        public CheckBoxAction RemoveOnButtonDown(Action action) {
            RemoveSignal(_onButtonDownAction, "button_down", nameof(_GodotSignalButtonDown), action);
            return this;
        }

        private CheckBoxAction _GodotSignalButtonDown() {
            ExecuteSignal(_onButtonDownAction);
            return this;
