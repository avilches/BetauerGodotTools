namespace Betauer.GodotAction {
    public class SkeletonIKAction : ProxyNode {

        private List<Action>? _onReadyAction; 
        public SkeletonIKAction OnReady(Action action, bool oneShot = false, bool deferred = false) {
            AddSignal(ref _onReadyAction, "ready", nameof(_GodotSignalReady), action, oneShot, deferred);
            return this;
        }

        public SkeletonIKAction RemoveOnReady(Action action) {
            RemoveSignal(_onReadyAction, "ready", nameof(_GodotSignalReady), action);
            return this;
        }

        private SkeletonIKAction _GodotSignalReady() {
            ExecuteSignal(_onReadyAction);
            return this;
        }

