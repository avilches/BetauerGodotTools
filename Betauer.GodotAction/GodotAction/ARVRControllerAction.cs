        public ARVRControllerAction RemoveOnUnhandledKeyInput(Action<InputEventKey> action) {
            _onUnhandledKeyInput?.Remove(action);
            return this;
        }

        public override void _Process(float delta) {
            if (_onProcessAction == null) {
                SetProcess(false);
                return;
            }
            for (var i = 0; i < _onProcessAction.Count; i++) _onProcessAction[i].Invoke(delta);
        }

        public override void _PhysicsProcess(float delta) {
            if (_onPhysicsProcess == null) {
                SetPhysicsProcess(true);
                return;
            }
            for (var i = 0; i < _onPhysicsProcess.Count; i++) _onPhysicsProcess[i].Invoke(delta);
        }

        public override void _Input(InputEvent @event) {
            if (_onInput == null) {
                SetProcessInput(true);
                return;
            }
            for (var i = 0; i < _onInput.Count; i++) _onInput[i].Invoke(@event);
        }

        public override void _UnhandledInput(InputEvent @event) {
            if (_onUnhandledInput == null) {
                SetProcessUnhandledInput(true);
                return;
            }
            for (var i = 0; i < _onUnhandledInput.Count; i++) _onUnhandledInput[i].Invoke(@event);
        }

        public override void _UnhandledKeyInput(InputEventKey @event) {
            if (_onUnhandledKeyInput == null) {
                SetProcessUnhandledKeyInput(true);
                return;
            }
            for (var i = 0; i < _onUnhandledKeyInput.Count; i++) _onUnhandledKeyInput[i].Invoke(@event);
        }

        private Action<int>? _onButtonPressedAction; 
        public ARVRControllerAction OnButtonPressed(Action<int> action) {
            if (_onButtonPressedAction == null) 
                Connect("button_pressed", this, nameof(ExecuteButtonPressed));
            _onButtonPressedAction = action;
            return this;
        }
        public ARVRControllerAction RemoveOnButtonPressed() {
            if (_onButtonPressedAction == null) return this; 
            Disconnect("button_pressed", this, nameof(ExecuteButtonPressed));
            _onButtonPressedAction = null;
            return this;
        }
        private void ExecuteButtonPressed(int button) =>
            _onButtonPressedAction?.Invoke(button);
        

